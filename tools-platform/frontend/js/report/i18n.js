    function register() {
        if (window.ToolsI18n && typeof window.ToolsI18n.register === 'function') {
            window.ToolsI18n.register('report', { 'zh-CN': zh, 'en-US': en });
        }
    }

    function t(key, params) {
        if (window.ToolsI18n && typeof window.ToolsI18n.t === 'function') {
            return window.ToolsI18n.t(key, params || {});
        }
        const raw = (zh[key] || key);
        return Object.keys(params || {}).reduce((acc, name) => acc.replaceAll(`{${name}}`, params[name]), raw);
    }

    function lang() {
        return window.ToolsI18n && typeof window.ToolsI18n.getLanguage === 'function'
            ? window.ToolsI18n.getLanguage()
            : 'zh-CN';
    }

    function setText(selector, key, params) {
        const el = document.querySelector(selector);
        if (el) el.textContent = t(key, params);
    }

    function setPlaceholder(selector, key) {
        const el = document.querySelector(selector);
        if (el) el.setAttribute('placeholder', t(key));
    }

    function sourceLabel(source) {
        if (source === 'sqlite') return 'SQLite';
        if (source === 'json') return 'JSON';
        if (source === 'auto') return t('report.source.auto');
        return source || '-';
    }

